package Enum;

/**
 * Estados dos Cavalos.
 * @author fm
 */
public enum HorseState {

    /**
     * Cavalo está no estábulo.
     */
    AT_THE_STABLE,

    /**
     * Cavalo está no paddock.
     */
    AT_THE_PADDOCK,

    /**
     * Cavalo está na linha de partida.
     */
    AT_THE_START_LINE,

    /**
     * Cavalo está a correr.
     */
    RUNNING,

    /**
     * Cavalo está na linha final.
     */
    AT_THE_FINNISH_LINE;
}

